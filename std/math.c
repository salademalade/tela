#include <math.h>

float tela_cos(float x)
{
  return cosf(x);
}

float tela_sin(float x)
{
  return sinf(x);
}

float tela_tan(float x)
{
  return tanf(x);
}

float tela_acos(float x)
{
  return acosf(x);
}

float tela_asin(float x)
{
  return asinf(x);
}

float tela_atan(float x)
{
  return atanf(x);
}

float tela_atan2(float x, float y)
{
  return atan2f(x, y);
}
